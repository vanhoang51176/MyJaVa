package vong_lap_for;

class InCacSoNhoHon3 {

	public static void main(String[] args) {
		int number = 0;
        
        /*
         * Trong trường hợp biến number còn nhỏ hơn 3
         * thì in ra giá trị của biến và sau đó tăng lên 1.
         */
        for (; number <3; ) {
            System.out.println(number);
            number++;
        }
	}

}
